#!/usr/bin/env python

class Calculator():
	def summa(self,num1,num2):
		result = num1+num2		
		return result
	def resta(self,num1,num2): 
		result = num1-num2
		return result
	def multiplica(self,num1,num2): 
		result = num1*num2
		return result
	def divide(self,num1,num2): 
		result = num1/num2
		return result
